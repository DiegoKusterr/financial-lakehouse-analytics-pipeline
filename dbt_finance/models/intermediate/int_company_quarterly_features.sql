    fundamental_ratios.company_quarter_key,
    fundamental_ratios.company_id,
    fundamental_ratios.ticker,
    fundamental_ratios.company_name,
    fundamental_ratios.sector,
    fundamental_ratios.industry,
    fundamental_ratios.country,
    fundamental_ratios.fiscal_quarter,
    fundamental_ratios.fiscal_quarter_start_date,
    fundamental_ratios.revenue,
