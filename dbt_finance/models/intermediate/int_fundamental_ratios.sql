select
    company_quarter_key,
    company_id,
    ticker,
    company_name,
    sector,
    industry,
    country,
    fiscal_quarter,
    fiscal_quarter_start_date,
    revenue,
    gross_profit,
    operating_income,
    net_income,
    total_assets,
    total_liabilities,
    equity,
    operating_cash_flow,
    previous_quarter_revenue,
    revenue_growth_qoq,
    gross_profit / nullif(revenue, 0) as gross_margin,
    operating_income / nullif(revenue, 0) as operating_margin,
    net_income / nullif(revenue, 0) as net_margin,
    total_liabilities / nullif(equity, 0) as debt_to_equity,
    net_income / nullif(total_assets, 0) as return_on_assets,
    operating_cash_flow / nullif(revenue, 0) as cash_flow_margin,
    revenue / nullif(total_assets, 0) as asset_turnover
from {{ ref('stg_quarterly_fundamentals') }}
